const saveFile = () => {
  const data = state.currentOutput; // 文件内容
  if (data.length == 0) {
    return
  }
  var today = new Date();
  var date = (today.getMonth() + 1) + '-' + today.getDate() + '-' + today.getHours() + "-" + today.getMinutes();
  var title = '评语' + date + '.txt'
  const blob = new Blob([data]) // 将数据封装在 Blob 对象中
  saveAs(blob, title) // 保存文件
}


